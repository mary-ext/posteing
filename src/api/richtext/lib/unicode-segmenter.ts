const bsearchRange = (
	x: number,
	table: [l: number, h: number, v?: unknown][],
	from = 0,
	to = table.length,
) => {
	let low = from;
	let high = to - 1;

	while (low <= high) {
		let mid = (low + high) >> 1;
		let cur = table[mid];

		let l = cur[0];
		let h = cur[1];

		if (l <= x && x <= h) {
			return mid;
		} else if (h < x) {
			low = mid + 1;
		} else {
			high = mid - 1;
		}
	}

	return -low - 1;
};

const bsearchUnicodeRange = (
	cp: number,
	table: GraphemeCategoryRange[],
	defaultLower: number,
	defaultUpper: number,
	from = 0,
	to = table.length,
) => {
	let found = bsearchRange(cp, table, from, to);
	if (found >= 0) {
		return table[found];
	}

	// Not found
	let low = -found - 1;
	let lower = low > 0 ? table[low - 1][1] + 1 : defaultLower;
	let upper = table[low] ? table[low][0] - 1 : defaultUpper;
	// @ts-ignore
	return [lower, upper, 0 /* Any */];
};

const isSMP = (cp: number): boolean => {
	// SMP (Supplementary Multilingual Plane)
	return 0xffff < cp;
};

const isIndicConjunctCosonant = (cp: number): boolean => {
	return bsearchRange(cp, CONSONANT_TABLE) >= 0;
};

const isIndicConjunctLinker = (cp: number): boolean => {
	return cp === 0x094d || cp === 0x09cd || cp === 0x0acd || cp === 0x0b4d || cp === 0x0c4d || cp === 0x0d4d;
};

const searchGraphemeCategory = (cp: number) => {
	// Perform a quick O(1) lookup in a precomputed table to determine
	// the slice of the range table to search in.
	let lookup_table = GRAPHEME_CATEGORY_LOOKUP;
	let lookup_interval = 0x80;

	let idx = (cp / lookup_interval) | 0;
	// If the `idx` is outside of the precomputed table - use the slice
	// starting from the last covered index in the precomputed table and
	// ending with the length of the range table.
	let sliceFrom = 1443,
		sliceTo = 1449;
	if (idx + 1 < lookup_table.length) {
		sliceFrom = lookup_table[idx];
		sliceTo = lookup_table[idx + 1] + 1;
	}

	// Compute pessimistic default lower and upper bounds on the category.
	// If character doesn't map to any range and there is no adjacent range
	// in the table slice - these bounds has to apply.
	let lower = idx * lookup_interval;
	let upper = lower + lookup_interval - 1;
	return bsearchUnicodeRange(cp, GRAPHEME_CATEGORY_TABLE, lower, upper, sliceFrom, sliceTo);
};

const cat = (cp: number, cache: GraphemeCategoryRange): GraphemeCategory => {
	if (cp < 127) {
		// Special-case optimization for ascii, except U+007F.  This
		// improves performance even for many primarily non-ascii texts,
		// due to use of punctuation and white space characters from the
		// ascii range.
		if (cp >= 32) {
			return GraphemeCategory.Any;
		} else if (cp === 10) {
			return GraphemeCategory.LF;
		} else if (cp === 13) {
			return GraphemeCategory.CR;
		} else {
			return GraphemeCategory.Control;
		}
	} else {
		// If this char isn't within the cached range, update the cache to the
		// range that includes it.
		if (cp < cache[0] || cp > cache[1]) {
			let result = searchGraphemeCategory(cp);
			cache[0] = result[0];
			cache[1] = result[1];
			cache[2] = result[2];
		}

		return cache[2];
	}
};

function isBoundary(
	catBefore: GraphemeCategory,
	catAfter: GraphemeCategory,
	risCount: number,
	emoji: boolean,
	incb: boolean,
): boolean {
	// GB3
	if (catBefore === GraphemeCategory.CR && catAfter === GraphemeCategory.LF) {
		return false;
	}

	if (
		// GB4
		catBefore === GraphemeCategory.CR ||
		catBefore === GraphemeCategory.Control ||
		catBefore === GraphemeCategory.LF ||
		// GB5
		catAfter === GraphemeCategory.CR ||
		catAfter === GraphemeCategory.Control ||
		catAfter === GraphemeCategory.LF
	) {
		return true;
	}

	// GB6
	if (
		catBefore === GraphemeCategory.L &&
		(catAfter === GraphemeCategory.L ||
			catAfter === GraphemeCategory.LV ||
			catAfter === GraphemeCategory.LVT ||
			catAfter === GraphemeCategory.V)
	) {
		return false;
	}

	// GB7
	if (
		(catBefore === GraphemeCategory.LV || catBefore === GraphemeCategory.V) &&
		(catAfter === GraphemeCategory.T || catAfter === GraphemeCategory.V)
	) {
		return false;
	}

	// GB8
	if (
		catAfter === GraphemeCategory.T &&
		(catBefore === GraphemeCategory.LVT || catBefore === GraphemeCategory.T)
	) {
		return false;
	}

	// GB9. GB9a
	if (
		catAfter === GraphemeCategory.Extend ||
		catAfter === GraphemeCategory.SpacingMark ||
		catAfter === GraphemeCategory.ZWJ
	) {
		return false;
	}

	// GB9b
	if (catBefore === GraphemeCategory.Prepend) {
		return false;
	}

	// GB9c
	if (catAfter === GraphemeCategory.Any && incb) {
		return false;
	}

	// GB11
	if (catBefore === GraphemeCategory.ZWJ && catAfter === GraphemeCategory.Extended_Pictographic) {
		return !emoji;
	}

	// GB12, GB13
	if (catBefore === GraphemeCategory.Regional_Indicator && catAfter === GraphemeCategory.Regional_Indicator) {
		return risCount % 2 === 0;
	}

	// GB999
	return true;
}

export const countGraphemes = (input: string): number => {
	if (input === '') {
		return 0;
	}

	let count = 0;

	/** Current cursor position. */
	let cursor = 0;

	/** Total length of the input string. */
	let len = input.length;

	/** Category of codepoint immediately preceding cursor, if known. */
	let catBefore: GraphemeCategory | null = null;

	/** Category of codepoint immediately preceding cursor, if known. */
	let catAfter: GraphemeCategory | null = null;

	let cache: GraphemeCategoryRange = [0, 0, GraphemeCategory.Control];

	/** The number of RIS codepoints preceding `cursor`. */
	let risCount = 0;

	/** Emoji state */
	let emoji = false;

	/** InCB=Consonant */
	let consonant = false;

	/** InCB=Linker */
	let linker = false;

	/** InCB=Consonant InCB=Linker x InCB=Consonant */
	let incb = false;

	/** @type number */
	let cp = input.codePointAt(cursor)!;

	// let index = 0;
	let segment = '';

	while (true) {
		segment += input[cursor++];
		if (isSMP(cp)) {
			segment += input[cursor++];
		}

		catBefore = catAfter;
		if (catBefore === null) {
			catBefore = cat(cp, cache);
		}

		if (!consonant && catBefore === 0) {
			consonant = isIndicConjunctCosonant(cp);
		} else if (catBefore === 3) {
			linker = isIndicConjunctLinker(cp);
		}

		if (cursor < len) {
			cp = input.codePointAt(cursor)!;
			catAfter = cat(cp, cache);
		} else {
			count++;
			return count;
		}

		if (catBefore === 10 /* Regional_Indicator*/) {
			risCount += 1;
		} else {
			risCount = 0;
			if (
				catAfter === 14 /* ZWJ */ &&
				(catBefore === 3 /* Extend */ || catBefore === 4) /* Extended_Pictographic */
			) {
				emoji = true;

				// Put GB9c rule checking here to reduce.
			} else if (catAfter === 0 /* Any */) {
				incb = consonant && linker && isIndicConjunctCosonant(cp);
			}
		}

		if (isBoundary(catBefore, catAfter, risCount, emoji, incb)) {
			count++;

			// flush
			// index = cursor;
			segment = '';
			emoji = false;
			consonant = false;
			linker = false;
		}
	}
};
